// Define available genres and rarities for random generation
const genres = [
  "Electronic", "Pop", "HipHop", "Rock", "Jazz", 
  "Classical", "Indie", "Metal", "EDM", "Country", 
  "Reggae", "Blues", "Folk", "Ambient"
];

const rarities = [
  "Common", "Uncommon", "Rare", "Epic", "Legendary"
];

// Rarity weights (higher number = less common)
const rarityWeights = {
  "Common": 50,
  "Uncommon": 30,
  "Rare": 15,
  "Epic": 4,
  "Legendary": 1
};

// Function to get a random music entry
export function getRandomMusic() {
  // First, determine the rarity based on weights
  const rarity = getWeightedRarity();
  
  // Then, get a random genre
  const genre = genres[Math.floor(Math.random() * genres.length)];
  
  // Filter music by genre and rarity
  const matchingMusic = musicData.filter(music => 
    music.genre === genre && music.rarity === rarity
  );
  
  // If we have matching music, return a random one
  if (matchingMusic.length > 0) {
    return matchingMusic[Math.floor(Math.random() * matchingMusic.length)];
  }
  
  // Fallback: just return any random music entry
  return musicData[Math.floor(Math.random() * musicData.length)];
}

// Function to get a weighted random rarity
function getWeightedRarity() {
  // Calculate total weight
  let totalWeight = 0;
  for (const rarity in rarityWeights) {
    totalWeight += rarityWeights[rarity];
  }
  
  // Get a random number between 0 and total weight
  const random = Math.floor(Math.random() * totalWeight);
  
  // Find which rarity this random number corresponds to
  let weightSum = 0;
  for (const rarity in rarityWeights) {
    weightSum += rarityWeights[rarity];
    if (random < weightSum) {
      return rarity;
    }
  }
  
  // Fallback
  return "Common";
}